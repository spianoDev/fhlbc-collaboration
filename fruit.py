# Initialized list of fruits
fruits = ["Apple", "Mango", "Banana", "Peach"]

# Print each fruit using for loop 
for fruit in fruits:
    print(fruit)
print("\n")

# Print each fruit using for loop and length of list
for fruit in range(0, len(fruits)):
    print(fruits[fruit])
print("\n")


# Print each fruit in the list using the length of list and while loop
i = 0
while(i < len(fruits)):
    print(fruits[i])
    i += 1
print("\n")